/**
 * import-gi-weapon-scaling.cjs
 *
 * Bakes EXACT per-level (1-90) base-ATK/secondary scaling + per-refinement (R1-R5)
 * passive scaling for every Genshin weapon, so the Weapon Inspector's level slider
 * and R1-R5 selector show real in-game values.
 *
 * Source: genshin-db `weapon.stats(level)` (exact attack + specialized per level) and
 * `weapon.r1..r5.values` (exact passive values per refinement).
 *
 * Compact representation: the normalized level curves (stat(L)/stat(90)) cluster into
 * a handful of distinct shapes across the roster, so we dedupe them into `atkCurves` /
 * `secCurves` (each a 90-length array) and store only a curve INDEX per weapon, plus a
 * 5-length `refine` multiplier (passive value at R_n ÷ R1). Level value = base90 ×
 * curve[L-1]; refinement value = passiveR1 × refine[R-1].
 *
 * Run: NODE_PATH=<scratchpad>/node_modules node scripts/import-gi-weapon-scaling.cjs
 * Emits: src/renderer/src/data/weapon-scaling.genshin-impact.generated.ts
 */
'use strict';
const fs = require('fs');
const path = require('path');
const gdb = require('genshin-db');

const OUT = path.join(__dirname, '..', 'src', 'renderer', 'src', 'data', 'weapon-scaling.genshin-impact.generated.ts');

// The weapon id set the app ships (full-name slugs) — read from the generated roster.
const weaponsTs = fs.readFileSync(path.join(__dirname, '..', 'adapters', 'game-definitions', 'genshin-impact', 'weapons.ts'), 'utf8');
const idNames = [];
// name may itself contain escaped quotes (e.g. "The Catch" is literally  "\"The Catch\"" ).
for (const m of weaponsTs.matchAll(/\{ id: "([^"]+)", name: ("(?:[^"\\]|\\.)*")/g)) idNames.push({ id: m[1], name: JSON.parse(m[2]) });

const num = (s) => { const m = String(s).match(/-?\d+(?:\.\d+)?/); return m ? +m[0] : null; };

// Deduping curve store: signature (rounded 90-vector) -> index.
function makeCurveStore() {
    const list = [], bySig = new Map();
    return {
        add(vec) {
            const sig = vec.join(',');
            if (bySig.has(sig)) return bySig.get(sig);
            const i = list.length; list.push(vec); bySig.set(sig, i); return i;
        },
        list,
    };
}
const atkStore = makeCurveStore();
const secStore = makeCurveStore();

const byId = {};
let matched = 0, missing = [];
for (const { id, name } of idNames) {
    const w = gdb.weapons(name);
    if (!w || typeof w.stats !== 'function') { missing.push(id); continue; }
    const s90 = (() => { try { return w.stats(90); } catch { return null; } })();
    if (!s90) { missing.push(id); continue; }
    const atk90 = s90.attack;
    const sec90 = s90.specialized;
    const atkNorm = [], secNorm = [];
    for (let L = 1; L <= 90; L++) {
        const s = (() => { try { return w.stats(L); } catch { return null; } })() || s90;
        atkNorm.push(atk90 ? +(s.attack / atk90).toFixed(5) : 0);
        secNorm.push(sec90 ? +(s.specialized / sec90).toFixed(5) : 1);
    }
    // Refinement multipliers from the first numeric passive value r1..r5.
    let refine = [1, 1, 1, 1, 1];
    const r1v = w.r1 && w.r1.values && num(w.r1.values[0]);
    if (r1v) {
        refine = ['r1', 'r2', 'r3', 'r4', 'r5'].map((rk) => {
            const rv = w[rk] && w[rk].values && num(w[rk].values[0]);
            return rv && r1v ? +(rv / r1v).toFixed(4) : 1;
        });
    }
    byId[id] = { a: atkStore.add(atkNorm), s: secStore.add(secNorm), refine };
    matched++;
}

const banner = `/**\n * Weapon level (1-90) + refinement (R1-R5) scaling for Genshin — AUTO-GENERATED by\n * scripts/import-gi-weapon-scaling.cjs (source: genshin-db). Do not edit by hand.\n *\n * atkCurves/secCurves: deduped 90-length normalized curves (value at level L = base90 * curve[L-1]).\n * byId[weaponId] = { a: atkCurveIdx, s: secCurveIdx, refine: [R1..R5 passive multipliers] }.\n */\n`;
const body =
    `export const atkCurves: number[][] = ${JSON.stringify(atkStore.list)};\n` +
    `export const secCurves: number[][] = ${JSON.stringify(secStore.list)};\n` +
    `export const byId: Record<string, { a: number; s: number; refine: number[] }> = ${JSON.stringify(byId)};\n`;
fs.writeFileSync(OUT, banner + '\n' + body);
console.log(`matched ${matched}/${idNames.length} weapons; ${missing.length} without genshin-db data (${missing.slice(0, 8).join(', ')}${missing.length > 8 ? '…' : ''}).`);
console.log(`deduped curves: ${atkStore.list.length} ATK, ${secStore.list.length} secondary.`);
console.log(`wrote ${path.relative(path.join(__dirname, '..'), OUT)}`);
